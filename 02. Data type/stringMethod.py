# String are immutable
a = "HArry Bhai"
print(len(a))  #find lenght of the string a
print(a.upper()) #convert to upper case
print(a.lower()) #convert to lower case
print(a.rstrip("Bhai")) #remove the all occurence of Bhai
print(a.replace("HArry", "John")) #replace to John from HArry
print(a.capitalize()) #first letter capital else small
print(a.split(" "))
print(a.find("y"))
print(a.swapcase())